export function Sidebar() {
  const pathname = usePathname()
  const { role, profile } = useAuth()

  // Determine which nav items to show based on role
  let navItems: NavItem[] = []
  if (role === 'admin') {
    navItems = adminNavItems
  } else if (role === 'coach') {
    navItems = coachNavItems
  } else if (role === 'user') {
    navItems = userNavItems
  }

  return (
    <aside className="hidden md:flex w-64 flex-col border-r bg-white">
      <nav className="flex-1 space-y-1 p-4">
        {navItems.map((item) => {
          // Exact match OR is a sub-route (but not for root dashboard routes)
          const isActive = pathname === item.href ||
            (pathname.startsWith(item.href + '/') && !item.href.match(/\/(user|coach|admin)$/))

          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors',
                isActive
                  ? 'bg-black text-white'
                  : 'text-gray-700 hover:bg-gray-100'
              )}
            >
              {item.icon}
              <span>{item.label}</span>
            </Link>
          )
        })}
      </nav>

      <div className="border-t p-4 h-1/5">
        <p className="text-xs text-gray-500">Version 1.0.0</p>
        <p className="text-xs text-gray-400 mt-1">
          {role ? `${role.charAt(0).toUpperCase() + role.slice(1)} Mode` : 'Loading...'}
        </p>
        {profile && (
          <p className="text-xs text-gray-500 mt-1">{profile.email}</p>
        )}
      </div>
    </aside>
  )
}
